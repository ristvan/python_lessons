# A variable is a container for storing data
# The = is used to assign values to variables
name = "John"  # String
age = 36  # Number: int
weight = 75.0  # Number: float


# The naming of the variable is case-sensitive and can only contain letters, numbers and underscores.
# The variable name cannot start with a number. The variable name cannot contain spaces.
# The variable name cannot be a reserved keyword. For example, 'and' is a reserved keyword in Python.
# The variable name in Python should be written in snake_case.
has_android_phone = True  # Boolean, True or False


print(name, age, weight, has_android_phone)


age = "thirty six"  # The type of the variable can be changed
print(name, age, weight, has_android_phone)
