# Function declarations start with 'def' and end with ':'.
# The function name is used to call the function.
# The function body is indented by 4 spaces. This is the standard.
# The function name is snake_case.
def hello_world():
   print("Hello, World!")




# Control flow statements are indented.
# The if statement starts with 'if' and ends with ':'.
# The if statement is followed by a condition.
# When the condition is true the code inside the if statement is executed.
# The __name__ variable is stands for the name of the current module.
if __name__ == '__main__':
   # Function is called here by using the name of the function followed by ().
   hello_world()
